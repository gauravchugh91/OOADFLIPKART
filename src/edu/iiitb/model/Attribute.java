package edu.iiitb.model;

public class Attribute {

	public String AttrName;
	public int AttrId;
	public String getAttrName() {
		return AttrName;
	}
	public void setAttrName(String attrName) {
		AttrName = attrName;
	}
	public int getAttrId() {
		return AttrId;
	}
	public void setAttrId(int attrId) {
		AttrId = attrId;
	}
	
}
